#include "../libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*ptr;

	i = 0;
	ptr = (char *)s;
	while (n > 0)
	{
		ptr[i] = c;
		i++;
		n--;
	}
	return (s);
}
